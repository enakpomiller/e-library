<h1 id="h1">About University of Calabar</h1>
<h2><img src="images/about-image.png" width="950" height="241" alt="" /></h2>
<p style="line-height:25px;">University of Calabar grew out of the Calabar campus of University of Nigeria (UNN), Nigeria which began functioning during the 1973 academic session with 154 students and a small cadre of academic, administration and professional staff. 
<p style="line-height:25px;">In April 1975, the Federal Military Government of Nigeria announced that as part of the National Development Plan, seven new Universities were to be established at various locations in the country. The University of Calabar was one of the seven Universities set up under this programme.</p>
<p style="line-height:25px;">Located in Calabar Municipal, an ancient city with a long tradition of culture and contact with western civilization, the already developed area of the University occupies a 17-hectare site on the eastern side of the town, between the Great Qua River and the Calabar River.
Additional land has been acquired on both sides of the Great Qua River for the development of the University.</p>
<p style="line-height:25px;">
Academic activities actually commenced in the 1973/74 session, in what was then a Campus of the University Of Nigeria, Nsukka (UNN).
However, the new autonomous institution started academic work with the faculties of Arts, Science and Social Science in October 1976 with an initial enrolment of 896 students.
Of this number, 406 students were carried over from the University of Nigeria. The teaching, administrative and technical staff either transferred from the University of Nigeria (UNN) or newly recruited, numbered about 156.<br /><br />At the beginning of the 1977/78 academic year, the Department of Education, formerly a part of Faculty of Arts was upgraded to the faculty of Education.
In 1978/79 academic year, the College of Medical Sciences was added, while the Faculty of Law, established during the 1979/80 academic year began offering courses in October 1980.
The Faculty of Agriculture was established during the 1980/81 academic year. Since then the University has been experiencing impressive growth.<br />Academic programmes of the University aim at laying a sound and broad undergraduate foundation upon which further intellectual and professional pursuits can be based at the Graduate school level.
As any other University in the Country, the University of Calabar places great premium not only on learning, but also on character. The emphasis on character is to help provide the Nation with learned Nigerian Men and Women with a sense of responsibility, crying out against Machiavellianism, bribery, nepotism, and other forms of corruption.</p>

<p style="line-height:25px;">To encourage good character, the University established an annual Pro-Chancellor's Prize for the best behaved student of the graduating class.
As a Federal institution, the University strives to bring about an atmosphere conducive to patriotic interaction between people from all parts of the world including Nigerians from all States and language groups.</p>

<p style="line-height:25px;">Therefore, all States of the federation are represented in the student population of the Institution.
On academic programming, the University adopts a flexible and forward-looking attitude. Thus, from its inception, the University adopted the four-year integrated degree programme based on the semester, and course system in the Faculties. The University also makes conscious use of local materials for instructional purposes, in the sincere effort to make her programmes relevant to the needs of Nigeria. Though not responsible to Cross River State in which it is located, the University is responsive to the genuine aspirations and interests of her immediate community and interacts freely with that community through her programmes in Adult and Continuing Education, comprehensive health scheme, as well as symposia, seminars and workshops organized frequently on and off-campus. </p>
<p style="line-height:25px;">Founded at the peak of the cultural renaissance of the country, and situated in an area of the country distinguished for her rich culture, the University prides itself as a significant academic custodian of the rich culture of the people through the performances of the Department of Theatre Arts, which has established an enviable record for itself within Nigeria and abroad. </p>

<p style="line-height:25px;">Founded in 1975 under the National Higher Education Expansion Programme of the Federal Military Government, the University of Calabar ranks among the leading and largest of Nigeria's second generation universities. It witnessed phenomenal physical, academic and staff growth in its first decade of existence. Those were the halcyon days of the petroleum revenue boom, the third quinquennial National Development Plan. </p>
<p style="line-height:25px;">From its nucleus on the Duke Town Campus, the University rapidly expanded into a busy academic community that is housed in a vast constellation of modern academic blocks, students' residential halls and staff quarters. The student population rose from 896 in 1976, spread in the Faculties of Arts, Science and Social Sciences to over 30,000 full-time and part-time degree, and diploma students in the 2001/2002 session. By the 2003/2004 session, some diploma programmes were phased out. Currently, the student population stands at 40,645.</p></p>
